package Day3;

public class Comparision {
	public static void main (String [] args) {
		char let = 'f';
		if ( let == 'a') {
			System.out.println(let + " letter is vowel");
		}
		else if ( let == 'e'){
			System.out.println(let + " letter is vowel");
		}
		else if ( let == 'i'){
			System.out.println(let + " letter is vowel");
		}
		else if ( let == 'o'){
			System.out.println(let + " letter is vowel");
		}
		else if ( let == 'u'){
			System.out.println(let + " letter is vowel");
		}
		else {
			System.out.println(let + " letter is not a vowel");
		}
		
	}
}
